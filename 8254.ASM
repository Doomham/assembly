	.MODEL TINY
	.CODE
	.STARTUP
	MOV	AX,0
	MOV	DS,AX
	MOV	CX,4
	IN	AL,61H
	OR	AL,3
	OUT	61H,AL
MAIN1:	
	MOV	BX,1000
	CALL	BEEP
	MOV	BX,1200
	CALL	BEEP
	MOV	BX,1400
	CALL	BEEP
	LOOP	MAIN1
	
	IN	AL,61H
	XOR	AL,3
	OUT	61H,AL
	.EXIT


BEEP 	PROC NEAR
	MOV	AX,34DCH
	MOV	DX,12H
	DIV	BX
	OUT	42H,AL
	MOV	AL,AH
	OUT	42H,AL
	CALL	WAITS
	RET
BEEP	ENDP

WAITS	PROC NEAR
	MOV	DX,6
	MOV	BX,0
	ADD	DX,DS:[46CH]
	ADC	BX,DS:[46EH]
WAIT1:
	MOV	BP,DS:[46CH]
	MOV	AX,DS:[46EH]
	SUB	BP,DX
	SBB	AX,BX
	JC	WAIT1
	RET
WAITS ENDP	
	END